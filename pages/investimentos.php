<p>O investimento na primeira infância não é apenas a coisa certa a ser feita do ponto de vista ético, mas também a coisa inteligente a ser feita do ponto de vista econômico para as crianças, bem como para suas famílias, suas comunidades e a sociedade em geral.</p>
<br>
<p>Os primeiros anos de uma criança apresentam uma janela única de oportunidade para abordar a desigualdade,  quebrar o ciclo da pobreza e melhorar uma ampla gama de resultados mais tarde na vida. Uma recente pesquisa do cérebro sugere a necessidade de abordagens holísticas para a aprendizagem, crescimento e desenvolvimento, reconhecendo que o bem-estar físico e intelectual de crianças pequenas, bem como seu desenvolvimento socio-emocional e cognitivo, está interrelacionado. Para aproveitar completamente futuras oportunidades na vida e tornarem-se membros produtivos da sociedade, as crianças, até ao final da primeira infância, devem ser: saudáveis e bem nutridas; devidamente apegadas a seus cuidadores; capazes de interagir positivamente com suas famílias, professores e colegas; capazes de se comunicar em sua língua nativa; e prontas para aprender no ensino primário.</p>
<br>
<p>Falhas no início do desenvolvimento infantil comprometem a capacidade da criança para atingir essas metas importantes e alcançar seu pleno potencial na vida. Intervenções nos primeiros anos têm o potencial de compensar as tendências negativas e fornecer às crianças mais oportunidades de uma qualidade ideal de aprendizagem, crescimento físico e saúde, e consequentemente aumentar sua produtividade ao longo da vida. Novas  evidências biológicas e sociais oferecem uma riqueza de recursos com estratégias inovadoras que promovem o desenvolvimento e crescimento ideal da criança. Programas que agregam serviços (tais como nutrição e estimulação psicossocial) podem ter efeitos e taxas de retorno extremamente benéficos. Infelizmente, a maioria dos países deixa a desejar em sua prestação de serviços essenciais para as crianças e suas famílias. O desafio é desenvolver modelos rentáveis e escaláveis para prestar estes serviços em países de baixa e média renda.</p>
<br>              
  <p>O desenvolvimento na primeira infância (DPI), se refere ao crescimento e desenvolvimento inicial da criança desde a gravidez de uma mulher até o ingresso da criança à escola primária.  Estes serviços são destinados a abordar a  saúde, necessidades nutricionais, socioemocionais, cognitivas e linguísticas durante este período. Eles são essenciais porque os primeiros anos de vida de uma criança constituem a base para a aprendizagem futura, boa saúde e bem-estar, bem como a capacidade da criança para trabalhar bem com os demais na vida adulta.</p>
