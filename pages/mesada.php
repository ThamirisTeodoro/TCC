<p> A mesada educativa é aquela voltada para ensinar seus filhos sobre economia, poupança, valor/preço e outras questões relacionadas ao universo financeiro. Assim, a principal vantagem dessa ideia é o envolvimento familiar..</p>
</p>
<br>
<p>Afinal, mais do que apenas oferecer uma quantia de dinheiro mensal aos seus filhos, você estará aproveitando esse momento para lhe passar ensinamentos, por exemplo, trabalhando a questão do consumo, ensinando-o a comparar o preço dos produtos, etc.</p>
<br>
<p>A mesada é um instrumento de educação financeira. Ela por si só não ensina nada. Junto com a mesada, os pais têm que dar orientação. É por meio da mesada que elas vão experimentar as situações típicas da vida adulta. Querer comprar alguma coisa e ela custar mais caro do que a sua renda. Precisar juntar duas ou três mesadas para atingir o objetivo. Gastar toda a mesada no começo e ficar sem dinheiro. Aprender a valorizar a mesada pedindo descontos, comparando preços.</p>
<br>
<p>A mesada não deve ser usada para comprar o lanche da escola, especialmente quando a criança ainda é pequena. Por quê? Para não correr o risco de a criança tomar a decisão de trocar o lanche pelo dinheiro. Na infância, a mesada também não deve contemplar roupas, sapatos e muito menos atividades extras como futebol, natação, inglês. Na adolescência, é diferente. Se o adolescente quer comprar roupas de grife, ele pode usar o dinheiro da mesada. Também não se deve trocar mesada pelas obrigações das crianças, como tirar boas notas na escola ou arrumar o seu quarto, os seus brinquedos. A criança tem que entender que tem deveres e isso não deve ser estimulado pelo dinheiro para que não se incentive a criação de uma personalidade mercenária.</p>
