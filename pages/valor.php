<p>O dinheiro significa possibilidade de troca, e de troca por algo que traz realização pessoal. O dinheiro não é a única possibilidade de conseguir algumas coisas, mas é a possibilidade mais viável para muitas formas de satisfação, e também a mais prático. O dinheiro não carrega consigo necessariamente a possibilidade de troca por felicidade e por satisfação, já que estes são estados internos. O dinheiro possibilita algumas formas de troca, algumas formas de simbolização e alguns acessos a situações mais satisfatórias. O dinheiro é um possibilitador. Com dinheiro posso viajar mais, mas se a viajem por si só não for suficiente ou satisfatória, se eu precisar de uma companhia genuína ou de uma causa para seguir, uma viajem não será garantia de nada disso. O dinheiro permite acesso a novos símbolos e novas formas de lidar com o que há de incerto sobre o desejo. É por isso que é esperado que pessoas mais ricas sejam mais felizes e satisfeitas com a vida.</p>
<br>
<p>Há uma questão ética sobre isso, para além da questão social da desigualdade. Considerar o dinheiro indispensável quando não se tem leva à infelicidade. Desconsiderar as felicidades trazidas pelo dinheiro quando se tem muito pode ser problemático, se não houver outras fontes de satisfação pessoal. É mais benéfico e mais importante considerar o dinheiro uma ferramenta nessa busca pela felicidade. Não é pela falta de uma ferramenta que uma tarefa é impossível. É preciso que pensemos o dinheiro como um meio, como uma contingência, como algo que não depende da vontade nem da atitude, porque atribuir a causa das infelicidades a algo que não controlamos nos coloca um beco sem saída. Tirar o dinheiro da posição de objeto do nosso desejo é uma escolha ética.</p>
<br>
<p> É importante que quem tem pouco dinheiro não valorize tanto isso de ter muito dinheiro, porque assim a pessoa que tem pouco busca outras formas de ser feliz. Se alguém muito pobre se investe muito em pensar nas felicidades trazidas pelo dinheiro as chances de essa pessoa ser muito infeliz são altas. Apesar das possibilidades de estudar, trabalhar, batalhar para vencer na vida, abrir um negócio, etc, a gente sabe que ganhar dinheiro e ter sucesso financeiro depende de uma infinidade de variáveis que fogem completamente ao nosso controle. Em suma, não há garantias de que estudando e trabalhando muito uma pessoa venha a se tornar rica.</p>
<br>              
  <p>Por outro lado, quem tem muito dinheiro e atribui um alto valor às possibilidades que o dinheiro proporciona tende a ser mais feliz. Isso é importante também, pois se aquela pessoa rica não tem boas relações familiares, ao focar sua vida sobre a construção desses laços as chances de infelicidade serão grandes, mesmo que seja alguém muito rico. As boas relações familiares são tão incertas e incontroláveis quanto o dinheiro. Dá pra perceber que o problema não é ter ou não dinheiro, o problema é o valor atribuído àquilo que se tem, ou não tem. Valorizar o que tem é se sentir de alguma forma satisfeito. Valorizar muito o que não se tem é uma ladeira rumo à infelicidade.</p>
